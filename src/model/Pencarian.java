package model;

public class Pencarian {
    private int id_pencarian;
    private String kata_kunci;
    private String makanan;
    private String minuman;

    public int getId_pencarian() {
        return id_pencarian;
    }

    public void setId_pencarian(int id_pencarian) {
        this.id_pencarian = id_pencarian;
    }

    public String getKata_kunci() {
        return kata_kunci;
    }

    public void setKata_kunci(String kata_kunci) {
        this.kata_kunci = kata_kunci;
    }

    public String getMakanan() {
        return makanan;
    }

    public void setMakanan(String makanan) {
        this.makanan = makanan;
    }

    public String getMinuman() {
        return minuman;
    }

    public void setMinuman(String minuman) {
        this.minuman = minuman;
    }
}
